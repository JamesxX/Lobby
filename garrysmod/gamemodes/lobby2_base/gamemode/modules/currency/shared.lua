local PlayerMeta = FindMetaTable("Player")

function PlayerMeta:GetMoney( )

	return tonumber( self:GetNWInt( "fMoney" ) )
	
end

function PlayerMeta:CanAfford( iAmount )

	return self:GetMoney() >= iAmount

end